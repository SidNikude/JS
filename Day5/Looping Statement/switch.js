var a = 5;
switch (a) {
    case 1:
        console.log("Value is 1 ");
        break;
    case 2:
        console.log("Value is 2 ");
        break;
    case 3:
        console.log("Value is 3 ");
        break;
    default:
        console.log("Default value ");
        break;
}