// 2. "if...else" Statement
//     if(condition OR expression)
//      { //true => block will get executed
//         // do something here
//         // block of code to be executed if condition is satisfied 
//      } 
//      else
//       { //false => block will get executed
//        // block of code to be executed if condition is not-satisfied 
//      }

var a = 19;

if (a>20)
{
    console.log("Value of a is " + a);
    console.log("a is greater than 20");
}

else
{
    console.log("Value of a is " + a);
    console.log("a is smaller than 20");
}