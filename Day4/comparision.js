// 2. Comparision Operator;
// not equal to: !=
// double equal to: ==
// tripple equal to: ===
// less than: <
// less than or qual: <=
// greater than: >
// greater than equal to: >=

value1 = 25;
value2 = 15;
value3 = 33;
value4 = 33;
value5 ="33";

// --------------------------- not equal to: != ---------------------------
console.log(value1 + " not equal to " + value2 + " result :" + (value1!=value2));
console.log(value3 + " not equal to " + value4 + " result :" + (value3!=value4));

// --------------------------- double equal to: == ------------------------
console.log(value1 + " double equal to " + value2 + " result :" + (value1==value2));
console.log(value3 + " double equal to " + value4 + " result :" + (value3==value4));
console.log(value4 + " double equal to " + value5 + " result :" + (value4==value5));

// --------------------------- triple equal to: === ------------------------
console.log(value1 + " triple equal to " + value2 + " result :" + (value1===value2));
console.log(value3 + " triple equal to " + value4 + " result :" + (value3===value4));
console.log(value4 + " triple equal to " + value5 + " result :" + (value4===value5));

// -----------------------------less than: < -------------------------------
console.log(value2 + " less than " + value1 + " result : " + (value2<value1));
console.log(value3 + " less than " + value4 + " result : " + (value3<value4));

// ---------------------------- less than or equal: <= ----------------------
console.log(value1 + " less than or equal to " + value2 + " result : " + (value1<=value2));
console.log(value2 + " less than or equal to " + value3 + " result : " + (value2<=value3));
console.log(value3 + " less than or equal to " + value4 + " result : " + (value3<=value4));

// ----------------------------- greater than: > -------------------------------
console.log(value2 + " greater than " + value1 + " result : " + (value2>value1));
console.log(value1 + " greater than " + value2 + " result : " + (value1>value2));

// ---------------------------- greater than or equal: >= ----------------------
console.log(value1 + " greater than or equal to " + value2 + " result : " + (value1>=value2));
console.log(value2 + " greater than or equal to " + value3 + " result : " + (value2>=value3));
console.log(value3 + " greater than or equal to " + value4 + " result : " + (value3>=value4));


