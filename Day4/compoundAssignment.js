// 4. Assignment Operator:
//     simple:
//     add and Assignment: +=
//     sub and Assignment: -=
//     mul and Assignment: *=
//     div and Assignment: /=
//     mod and Assignment: %=
    
num1 = 25;
num2 = 15;
num3 = 35;
num4 = 20;
num5 = 40;

//--------------------- add and Assignment: += ------------------------
console.log("Original value of num1 is " + num1);
num1 += 5; // num1 = num1 + 5;
console.log("num1 after adding 5 : " + num1);

//--------------------- sub and Assignment: -= ------------------------
console.log("Original value of num2 is " + num2);
num2 -= 5; // num2 = num2 - 5;
console.log("num2 after subtracting 5 : " + num2);

//--------------------- mul and Assignment: *= ------------------------
console.log("Original value of num3 is " + num3);
num3 *= 5; // num3 = num3 * 5;
console.log("num3 after multiplying with 5 : " + num3);

//--------------------- div and Assignment: /= ------------------------
console.log("Original value of num4 is " + num4);
num4 /= 5; // num4 = num4 / 5;
console.log("num4 after dividing by 5 : " + num4);

//--------------------- mod and Assignment: %= ------------------------
console.log("Original value of num5 is " + num5);
num5 %= 5; // num5 = num5 % 5;
console.log("num5 after using modulus of 5 : " + num5);